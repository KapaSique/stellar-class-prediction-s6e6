import pandas as pd, numpy as np
train = pd.read_csv("data/train.csv")
test = pd.read_csv("data/test.csv")
print("train", train.shape, "test", test.shape)
print("\ncolumns:", list(train.columns))
print("\n=== class balance ===")
print(train["class"].value_counts())
print((train["class"].value_counts(normalize=True)*100).round(2))
print("\n=== dtypes ===")
print(train.dtypes)
print("\n=== missing (train) ===")
print(train.isna().sum()[train.isna().sum()>0])
print("\n=== categoricals ===")
for c in ["spectral_type","galaxy_population"]:
    print(f"\n{c}: {train[c].nunique()} uniq")
    print(train[c].value_counts().head(8))
print("\n=== redshift by class (key feature) ===")
print(train.groupby("class")["redshift"].describe()[["mean","50%","min","max"]])
print("\n=== test has 'class'? ===", "class" in test.columns)
print("test columns:", list(test.columns))
